package moviebookingfx.Class;


public abstract class Screen {
    private int price;
    private String name;
    
    Screen(){
       
    }
    
    public abstract void init();
    
    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
